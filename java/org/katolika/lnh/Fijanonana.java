package org.katolika.lnh;

public class Fijanonana {
    private int id;
    private String lohateny;
    private String fampidirana;
    private String vavaka;

    public Fijanonana() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLohateny() {
        return lohateny;
    }

    public void setLohateny(String lohateny) {
        this.lohateny = lohateny;
    }

    public String getFampidirana() {
        return fampidirana;
    }

    public void setFampidirana(String fampidirana) {
        this.fampidirana = fampidirana;
    }

    public String getVavaka() {
        return vavaka;
    }

    public void setVavaka(String vavaka) {
        this.vavaka = vavaka;
    }
}
